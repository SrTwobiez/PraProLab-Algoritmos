<?php

/* Para $i <- 0 Hasta ser menor o igual a 10 añadir 1. */

for ($i=0; $i<=10; $i++) { 
    echo "Número ", $i, "<br/>";
}

echo "--------------", "<br/>";

/* El número inicial puede ser cualquiera, por ejemplo:
Para $i <- 5 Hasta ser menor o igual a 10 añadir 1. */

for ($i=5; $i<=10; $i++) { 
    echo "Número ", $i, "<br/>";
}

?>